function javaScript (num1, num2, num3) {
  if (Math.round((num1 + num2 + num3)/3) === 1){
    return true
  }
}
function Python (num1, num2, num3) {
  if (Math.round((num1, num2, num3)/3) === 2){
    return true
  }
}
function cSharp (num1, num2, num3) {
  if (Math.round((num1, num2, num3)/3)=== 3){
    return true
  }
}